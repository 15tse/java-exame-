package stringandsb;
import java.lang.String;
public class Concatination {
	public static void main(String[] args) {
		String city = "New York";
		String country = "USA";
		String location = city + ", " + country + " is a great place to visit.";
		System.out.println(location);  // Outputs: New York, USA is a great place to visit.


    }


}

