package myexcers;

public class temporaryvariable {
	  public static void main(String[] args) {
	        int a = 5;
	        int b = 10;

	      System.out.println("a="+a +" ,b=" + b);
	      //Temporary method 
	      int tmp=a;
	      a=b;
	      b=tmp;
	      System.out.println("a="+a +" ,b="+b);
	    }
	}



